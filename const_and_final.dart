

/// Const and Final
// Both are immutable ( value that can not change )


/// Const :
// 1. It is compile time constant value that never get change
// 2. perform  DateTime.now(); is not possible
// 3. initialize at compile time
// 4. cannot use runtime values


/// Final :
// 1. It is Run time constant
// 2. perform DateTime.now(); is possible
// 3. initialize at run time
// 4. can be assign dynamically at runtime [ user input, API response ]



// use :  values fetch from the server or database, want to read the local files, to get network request that can not be changed